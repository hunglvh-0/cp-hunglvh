inline void solve() {
    int t;cin>>t;
    while(t--){
        int n;cin>>n;
        vector<ll> a(n);
        for(int i=0;i<n;i++)cin>>a[i];
        ll l=0,r=200000000000000,ans=r;
        while(l<=r){
            ll m=l+(r-l)/2;
            ll b=m;
            bool ok=true;
            for(int i=n-1;i>=0;i--){
                if(a[i]<=b){
                    b=max(a[i],b-a[i]);
                }
                else{
                    b-=a[i];
                }
                if(b<0){
                    ok=false;break;
                }
            }
            if(ok){ans=m;r=m-1;}
            else {
                l=m+1;
            }
        }
        cout<<ans<<el;
    }
}
